def get_t_data(tanda_label):
    """
    Devuelve la transmisión atmosférica (Cn2) en función de elevación
    para una tanda especificada.

    Parameters
    ----------
    tanda_label : str
        Etiqueta de la tanda, p.e. 'tanda3_14.05'.

    Returns
    -------
    elev : array-like
        Ángulos de elevación (0–90°).
    cn2_vals : array-like
        Valores de Cn2 para esa tanda.
    """
    if tanda_label not in tandas:
        raise ValueError(f"Etiqueta '{tanda_label}' no encontrada en tandas")
    idx = np.where(tandas == tanda_label)[0][0]
    return elev, cn2[:, idx]
